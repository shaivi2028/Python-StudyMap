# Subscripting
#pulling out characters from the string

print("Hello"[0])
print("Hello"[4])
print("Hello"[-1]) #Negative one signifies last character of the string

#String
# numbers written in double quotes are treated as strings

print("123" + "345")

#Integer

print(123456)
print(123 + 345)
# like we use commas in numeric system, we use "_" here
print(10_345 + 1_234) # 10,345 + 1,234

#Floats
print(3.14159)

#Boolean
#Forst letter should be capital

print(True)
print(False)